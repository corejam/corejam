@Component({
  tag: "corejam-canvas-editor",
})
export class CjDebugger {
  @State() max = false;
  @State() machineId: string;
  @Prop() draggers: string[] = ["corejam-box", "corejam-type", "corejam-test"];

  async deploy(e: PointerEvent) {
    e.preventDefault();

    const styleTags = document.querySelectorAll("style");
    const stringifiedHtml = document.getElementsByTagName("corejam-canvas")[0].outerHTML;
    const name = window.prompt("name");

    if (name) {
      const request = await coreState.client.mutate({
        mutation: gql(PostCanvasGQL),
        variables: {
          canvasPageInput: {
            canvas: JSON.stringify(templateFnc(stringifiedHtml, styleTags, name)),
            seo: {
              url: name,
            },
          },
        },
      });

      if (request.data.canvasPageCreate.id) {
        runState.routes = [
          {
            url: `/${name}`,
            canvasPage: true,
            component: JSON.stringify(stringifiedHtml),
            exact: true,
          },
        ];

        routerState.router.push(`/${name}`);
      }
    }
  }

  toggleMenu() {
    console.log("toggle");
    if (this.max) {
      sendEventToMachine({ type: "reset" });
    }
    this.max = !this.max;
  }

  render() {
    const activeEditor = !canvasState.machine.matches("inactive");
    return (
      <Host>
        <corejam-box position="fixed" p={4} h={activeEditor ? "400px" : "80px"} bottom={5} w={12}>
          <corejam-box position="absolute" bottom={25} left={25} onClick={() => this.toggleMenu()} z={300}>
            <Corejam w="30px" h="30px" />
          </corejam-box>
          <corejam-box
            w={12}
            h="100%"
            mx="auto"
            rounded="lg"
            p={4}
            pl={16}
            animation="all"
            show={this.max ? "flex" : "none"}
          >
            <corejam-tabs>
              <corejam-tab header="Builder" activeFn={() => sendEventToMachine({ type: "build" })}>
                <corejam-box flex direction="col" p={4} class="draggers">
                  {this.draggers.map((dragItem) => {
                    const Tag = dragItem;
                    return (
                      <corejam-box
                        py={2}
                        onPointerDown={sendEventToMachine}
                        data-component={dragItem}
                        style={{ userSelect: "none" }}
                      >
                        <Tag style={{ display: "none" }}></Tag>
                        {dragItem}
                      </corejam-box>
                    );
                  })}
                </corejam-box>
              </corejam-tab>
              <corejam-tab header="Deployment" activeFn={() => sendEventToMachine({ type: "deployment" })}>
                <corejam-box flex direction="col" pt={24}>
                  <corejam-type as="h3" size="lg" weight="hairline">
                    Deploy to CDN
                  </corejam-type>
                  <corejam-button
                    w={6}
                    bg="green-200"
                    hoverBg="green-300"
                    p="2"
                    color="gray-700"
                    hoverColor="white"
                    onPointerUp={this.deploy}
                  >
                    Deploy
                  </corejam-button>
                </corejam-box>
              </corejam-tab>
              <corejam-tab header="Settings"></corejam-tab>
              <corejam-tab header="Debug"></corejam-tab>
            </corejam-tabs>
          </corejam-box>
        </corejam-box>
      </Host>
    );
  }
}
